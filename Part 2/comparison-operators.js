// comparison operators akan menghasilkan boolean
// comparison operators tidak akan menghasilkan 'object'

// == perbandingan tanpa melihat tipe data
console.log(5 == 5) // true
console.log(5 == '5') // true

// == perbandingan melihat tipe data
console.log(5 === 5) // true
console.log(5 === '5') // false

// > lebih dari
console.log(5 > 3) // true

// < kurang dari
console.log(3 < 5) // true

// >= lebih dari sama dengan
console.log(5 >= 5) // true


